#Program draws four circles in the center of the screen
import turtle

turtle.color("red")
turtle.penup()
turtle.goto(-100, 100)
turtle.pendown()
turtle.circle(100)
turtle.penup()
turtle.goto(-100, -100)
turtle.pendown()
turtle.circle(100)
turtle.penup()
turtle.goto(100, 100)
turtle.pendown()
turtle.circle(100)
turtle.penup()
turtle.goto(100, -100)
turtle.pendown()
turtle.circle(100)
